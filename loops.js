                                                     //forExample

function myFunction() {
  var text = "";
  for (var i = 0; i < 5; i++) {
    text += "The number is " + i + "<br>";
  }
  document.write(text);
}
myFunction();


                                                    //whileExample


function myFunction() {
  var text = "";
  var i = 0;
  while (i < 5) {
    text += "<br>The number is " + i;
    i++;
  }
  document.write(text);
}
myFunction();


